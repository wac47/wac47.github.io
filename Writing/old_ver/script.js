var posMap;

$(function(){
  setting(aurebesh);
  $("#btn").submit(function(){
    if($("#answer").val()==posMap[que][0]){
      $("#answer").attr("placeholder","");
      setting();
    }else{
      $("#answer").attr("placeholder",posMap[que][0]);
      $("#answer").val("").select();
    }
    return false;
  });
});

function setting(lang){
  if(lang){
    posMap=lang;
    $("#question").attr("src",posMap[0][1]);
    $("#quebox").css({
      width:posMap[0][2]+"px",
      height:posMap[0][3]+"px"
    });
    $("title").html(posMap[0][0]+" Learning");
  }
  que=Math.floor(Math.random(Date.now())*(posMap.length-1))+1;
  $("#question").css({
    top:"-"+posMap[que][1]+"px",
    left:"-"+posMap[que][2]+"px",
  });
  $("#answer").val("").select();
}

